$title{Sons of the Highland Manse}

$blockquote{
Dugald, the Rev. Charles' youngest son, and apparently a "stickit minister" had become master of his own brigantine the "Annabella" in which he conveyed his sister Annabella and her husband Robert and family along with many Kintyre settlers to Prince Edward Island in 1770. Among the names of passengers were $b{Galbraith}, McMillan, McNeill, Montgomery, McGougan, McArthur, McDougall and McKay.
