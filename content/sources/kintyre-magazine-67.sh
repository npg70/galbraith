$blockquote{
The Greenlees family came to Kintyre in about 1640, when a miller of that name moved from Lochwinnoch (Renfrewshire) to Southend. In 1810, James Greenlees, farmer at Peninver (north of Campbeltown), married $b{Catherine Galbreath}. Peninver was the farm originally worked by Catherine's father, $b{Samuel Galbreath}. The couple had 11 children, the first born son (b. 1812) being named Samuel, after James's father. James died in 1850 leaving £186 18s 7 d, of which £137 18s 9d represented cattle, horses and farm implements.
}

