title: Which Janet Galbraith married Hugh Mitchell?


Janet Galbraith, who married Hugh Mitchell, is the daughter of Samuel Galbreath and Janet McNair.

This made sense since there was Janet Galbreath b.  Hwoever almost nothing else is know about this family.  All records besides birth and marriage lead no where.  They are no present in any census records.  Nor do I see any death records.

When investigating my own ancestor, $child-link[galbreath-samuel-1736-mcnair]{Samuel Galbreath}, I noticed something unsual in The Census of 1792.  A Janet Galbreath is listed, age 7.  This Janet was new.  And the baptism record appears to be lost (or not recorded).