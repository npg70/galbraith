After his conviction, there were issue with him improving the house that was in trust for wife, using effectively stolen monies.$ref[legal1]

Interestingly, there was also a ship "James Nicol Fleming", launched in 1869. It was built by Albion Shipping, owned by James Galbraith of Glasgow$ref[ship1]. In 1879, the name was changed to "Napier" (also a name related to the Galbraiths).

Prior to this James Galbraith, launched the ship "Margaret Galbraith" in 1868,$ref[ship2] named after this wife.

Just a conciendence that Fleming's wife was a Galbraith, and the Galbraith shipbuilder named his ship after Fleming? I assumed Fleming approved the loan for the ship.

